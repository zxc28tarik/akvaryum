const BASE_PARAMS = Object.freeze({ pH: [6, 7], temp: [22, 24], gh: [2, 5] });

const fish = (id, overrides = {}) => ({ id, ...overrides });

const expected = (
  issues,
  warnings,
  tips,
  compat,
  params,
  score,
  verdict,
  neededVol,
  bioloadPct,
  totalFish,
  totalSpecies,
) => ({
  issues,
  warnings,
  tips,
  compat,
  params,
  score,
  verdict,
  neededVol,
  bioloadPct,
  totalFish,
  totalSpecies,
});

const analysis = (id, purpose, definitions, state, result, extras = {}) => ({
  id,
  mode: 'analysis',
  purpose,
  fish: definitions,
  plants: extras.plants ?? [],
  substrates: extras.substrates ?? [],
  state,
  expected: result,
});

const equipment = (id, purpose, state, equipmentAnalysis, result, extras = {}) => ({
  id,
  mode: 'equipment',
  purpose,
  fish: [],
  plants: extras.plants ?? [],
  substrates: extras.substrates ?? [],
  state,
  analysis: equipmentAnalysis,
  expected: result,
});

